#include "libft.h"

char	*ft_strdup(const char *str, size_t n)
{
	char	*s;

	s = (char *)malloc((n + 1) * sizeof(char));
	if (!s)
		return (0);
	ft_memcpy(s, str, n);
	s[n] = '\0';
	return (s);
}
